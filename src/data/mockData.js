// Generates a plausible price walk for sparklines
function priceWalk(base, steps, volatility = 0.003) {
  const pts = [base]
  for (let i = 1; i < steps; i++) {
    const change = pts[i - 1] * volatility * (Math.random() - 0.48)
    pts.push(+(pts[i - 1] + change).toFixed(2))
  }
  return pts
}

